// Build reverse mapping: slug -> pokopia_id
export const SLUG_TO_POKOPIA_ID = Object.fromEntries(
  Object.entries(POKOPIA_DEX).map(([id, slug]) => [slug, parseInt(id)])
)

// Get Pokopia dex ID for a slug, returns null if not in Pokopia dex
export function getPokopiaId(slug) {
  return SLUG_TO_POKOPIA_ID[slug] ?? null
}

// Check if a slug is in the Pokopia dex
export function isInPokopiaDex(slug) {
  return slug in SLUG_TO_POKOPIA_ID
}
